const https = require('https');

const API_KEY = process.env.NOTION_TOKEN;
if (!API_KEY) {
    console.error('NOTION_TOKEN environment variable is required.');
    process.exit(1);
}

const DATABASE_ID = "30f556a1-98c7-8164-a3a2-f04b8ffd0a8b";
const CALLOUT_ICON_URL = "https://www.notion.so/icons/document_gray.svg";

const headers = {
    'Authorization': 'Bearer ' + API_KEY,
    'Notion-Version': '2022-06-28',
    'Content-Type': 'application/json'
};

function request(method, path, data) {
    return new Promise((resolve, reject) => {
        const req = https.request({
            hostname: 'api.notion.com',
            path,
            method,
            headers
        }, res => {
            let body = '';
            res.on('data', chunk => body += chunk);
            res.on('end', () => {
                if (res.statusCode >= 200 && res.statusCode < 300) {
                    resolve(JSON.parse(body));
                } else {
                    reject(new Error('HTTP ' + res.statusCode + ': ' + body.substring(0, 200)));
                }
            });
        });
        req.on('error', reject);
        req.write(JSON.stringify(data));
        req.end();
    });
}

